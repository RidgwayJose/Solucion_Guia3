package com.senati.eti;

public class Caso1 {

	public static void main(String[] args) {
		// Estructuras Repetitivas
		// Mientras - While
		// Para     - For
		int x = 1;
		
		while(x <= 5) {
			System.out.println("Bienvenido " + x);
			x++;
		}
		System.out.println("\nWhile - Contador descendente");
		System.out.println("------------------------------");
		
		int c = 5;
		
		while (c > 0) {
			System.out.println("Bienvenido " + c);
			c--;
		}
	}

}
